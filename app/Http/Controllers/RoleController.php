            'description'=>$request->description,
        ]);

        if($success){
            $all_roles=Role::all();
            return ['return'=>1,'html'=>view('admin.roles')->with(['all_roles'=>$all_roles])->render()];
        }
        else{
            return ['return'=>0,'html'=>""];
        }
    }


